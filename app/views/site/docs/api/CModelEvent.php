<h2>Property Details</h2>
<div class="detailHeader" id="isValid-detail">
isValid<span class="detailHeaderTag">
property
</span>
</div>

<div class="signature">
public boolean <b>$isValid</b>;</div>

<p>whether the model is in valid status and should continue its normal method execution cycles. Defaults to true.
For example, when this event is raised in a <?php echo CHtml::link('CFormModel', array('/site/doc', 'view' => 'CFormModel')); ?> object that is executing <?php echo CHtml::link('CModel::beforeValidate', array('/site/doc', 'view' => 'CModel', '#' => 'beforeValidate')); ?>,
if this property is set false by the event handler, the <?php echo CHtml::link('CModel::validate', array('/site/doc', 'view' => 'CModel', '#' => 'validate')); ?> method will quit after handling this event.
If true, the normal execution cycles will continue, including performing the real validations and calling
<?php echo CHtml::link('CModel::afterValidate', array('/site/doc', 'view' => 'CModel', '#' => 'afterValidate')); ?>.</p>


