<div id="classDescription">
CEvent is the base class for all event classes.
<br/><br/>
It encapsulates the parameters associated with an event.
The <?php echo CHtml::link('sender', array('/site/doc', 'view' => 'CEvent', '#' => 'sender')); ?> property describes who raises the event.
And the <?php echo CHtml::link('handled', array('/site/doc', 'view' => 'CEvent', '#' => 'handled')); ?> property indicates if the event is handled.
If an event handler sets <?php echo CHtml::link('handled', array('/site/doc', 'view' => 'CEvent', '#' => 'handled')); ?> to true, those handlers
that are not invoked yet will not be invoked anymore.</div>
